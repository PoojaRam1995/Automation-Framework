package pageobjectmodelclass;

public class PageObjectModel {
	
// POM-Page Object Model	: It is also known as POM.It is a design pattern in selenium
//	that creates an object repository for storing the web elements.It is used in reducing code duplication and improve the test  case 
//	 maintanence.
	
//	Inside the POM we consider each webpage as a separate class file.Each class has its own webpage elements.Using these elements
//	we can  perform the operations. By calling the different methods  inside the test case.
	
//  In a nut shell POM is a design pattern in which we keep the page  and test classes separately.
//  All the functionalities  we write into the page class and we can call those inside the test classes as a test steps.
	
	
	
	
}
